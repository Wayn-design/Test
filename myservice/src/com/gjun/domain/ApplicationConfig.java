package com.gjun.domain;

public class ApplicationConfig {
	private String sakilaDao;
	private String gjunDao;
	private String companyName;
	private String address;
	private String phone;
	public String getSakilaDao() {
		return sakilaDao;
	}
	public void setSakilaDao(String sakilaDao) {
		this.sakilaDao = sakilaDao;
	}
	public String getGjunDao() {
		return gjunDao;
	}
	public void setGjunDao(String gjunDao) {
		this.gjunDao = gjunDao;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	
	
}
